#ifndef FONT_ENCODING_H
#define FONT_ENCODING_H

/*
 * Font character encoding macros for Snowboard Kids 2.
 *
 * Text encoding format:
 * - 16-bit big-endian values
 * - Bits 12-15: Character width in pixels (0 = default 12, 8 = small)
 * - Bits 0-11: Character index into font table (0-93)
 *
 * Control codes use 0xFF in the high byte.
 */

/* Control codes */
#define __       0xFF, 0xFB
#define _END     0xFF, 0xFF
#define _NEWLINE 0xFF, 0xFD
#define _SKIP    0xFF, 0xFC

/* Big uppercase letters (indices 10-35) - default width (12) */
#define _A  0x00, 0x0A
#define _B  0x00, 0x0B
#define _C  0x00, 0x0C
#define _D  0x00, 0x0D
#define _E  0x00, 0x0E
#define _F  0x00, 0x0F
#define _G  0x00, 0x10
#define _H  0x00, 0x11
#define _I  0x00, 0x12
#define _J  0x00, 0x13
#define _K  0x00, 0x14
#define _L  0x00, 0x15
#define _M  0x00, 0x16
#define _N  0x00, 0x17
#define _O  0x00, 0x18
#define _P  0x00, 0x19
#define _Q  0x00, 0x1A
#define _R  0x00, 0x1B
#define _S  0x00, 0x1C
#define _T  0x00, 0x1D
#define _U  0x00, 0x1E
#define _V  0x00, 0x1F
#define _W  0x00, 0x20
#define _X  0x00, 0x21
#define _Y  0x00, 0x22
#define _Z  0x00, 0x23

/* Big digits (indices 0-9) - default width */
#define _0  0x00, 0x00
#define _1  0x00, 0x01
#define _2  0x00, 0x02
#define _3  0x00, 0x03
#define _4  0x00, 0x04
#define _5  0x00, 0x05
#define _6  0x00, 0x06
#define _7  0x00, 0x07
#define _8  0x00, 0x08
#define _9  0x00, 0x09

/* Small lowercase letters (indices 46-71) - width 8 */
#define _a  0x80, 0x2E
#define _b  0x80, 0x2F
#define _c  0x80, 0x30
#define _d  0x80, 0x31
#define _e  0x80, 0x32
#define _f  0x80, 0x33
#define _g  0x80, 0x34
#define _h  0x80, 0x35
#define _i  0x80, 0x36
#define _j  0x80, 0x37
#define _k  0x80, 0x38
#define _l  0x80, 0x39
#define _m  0x80, 0x3A
#define _n  0x80, 0x3B
#define _o  0x80, 0x3C
#define _p  0x80, 0x3D
#define _q  0x80, 0x3E
#define _r  0x80, 0x3F
#define _s  0x80, 0x40
#define _t  0x80, 0x41
#define _u  0x80, 0x42
#define _v  0x80, 0x43
#define _w  0x80, 0x44
#define _x  0x80, 0x45
#define _y  0x80, 0x46
#define _z  0x80, 0x47

/* Small digits (indices 36-45) - width 8 */
#define _0s 0x80, 0x24
#define _1s 0x80, 0x25
#define _2s 0x80, 0x26
#define _3s 0x80, 0x27
#define _4s 0x80, 0x28
#define _5s 0x80, 0x29
#define _6s 0x80, 0x2A
#define _7s 0x80, 0x2B
#define _8s 0x80, 0x2C
#define _9s 0x80, 0x2D

/* Symbols (indices 72-93) - width 8 */
#define _EXCL  0x80, 0x48  /* ! */
#define _DQUOTE 0x80, 0x49 /* " */
#define _HASH  0x80, 0x4A  /* # */
#define _GRAVE 0x80, 0x4B  /* ` */
#define _STAR  0x80, 0x4C  /* * */
#define _PLUS  0x80, 0x4D  /* + */
#define _COMMA 0x80, 0x4E  /* , */
#define _MINUS 0x80, 0x4F  /* - */
#define _DOT   0x80, 0x50  /* . */
#define _SLASH 0x80, 0x51  /* / */
#define _COLON 0x80, 0x52  /* : */
#define _QUEST 0x80, 0x53  /* ? */
#define _QUOTE 0x80, 0x54  /* close quote/@ */
#define _AT    0x80, 0x55  /* box corner */
#define _BOX1  0x80, 0x56  /* box corner */
#define _TILDE 0x80, 0x58  /* ~ */
#define _LPAREN 0x80, 0x59 /* ( */
#define _RPAREN 0x80, 0x5A /* ) */
#define _AMP   0x80, 0x5B  /* & */
#define _MDOT  0x80, 0x5C  /* middle dot */
#define _CIRC  0x80, 0x5D  /* circle with exclamation */

#endif /* FONT_ENCODING_H */
